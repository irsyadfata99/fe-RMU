export function calculateStockStatus(stock: number, minStock: number, maxStock: number): StockStatus {
  if (stock === 0) return StockStatus.EMPTY;
  if (stock <= minStock) return StockStatus.LOW;
  if (stock >= maxStock) return StockStatus.OVER;
  return StockStatus.NORMAL;
}

